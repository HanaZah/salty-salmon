-- =============================================
-- 1. GEOGRAPHY (CITIES & STREETS)
-- =============================================

CREATE TABLE IF NOT EXISTS CITIES (
                        CITY_ID INTEGER NOT NULL,
                        NAME    VARCHAR2(100 CHAR) NOT NULL,
                        PSC     VARCHAR2(6 CHAR) NOT NULL,
                        CONSTRAINT CITY_PK PRIMARY KEY (CITY_ID),
                        CONSTRAINT CITY_NAME_PSC_UN UNIQUE (NAME, PSC),
                        CONSTRAINT CITY_PSC_CK CHECK (REGEXP_LIKE(PSC, '^\d{3}\s\d{2}$'))
);
/

CREATE TABLE IF NOT EXISTS STREETS (
                         STREET_ID INTEGER NOT NULL,
                         NAME      VARCHAR2(100 CHAR) NOT NULL,
                         CITY_ID   INTEGER NOT NULL,
                         CONSTRAINT STREET_PK PRIMARY KEY (STREET_ID),
                         CONSTRAINT STREET_NAME_CITY_UN UNIQUE (NAME, CITY_ID),
                         CONSTRAINT STREET_CITY_FK FOREIGN KEY (CITY_ID) REFERENCES CITIES (CITY_ID)
);
/

CREATE TABLE IF NOT EXISTS ADDRESSES (
                           ADDRESS_ID         INTEGER NOT NULL,
                           HOUSE_NUMBER       VARCHAR2(10 CHAR) NOT NULL,
                           STREET_ID          INTEGER NOT NULL,
                           CONSTRAINT ADDRESS_PK PRIMARY KEY (ADDRESS_ID),
                           CONSTRAINT ADDRESS_STREET_FK FOREIGN KEY (STREET_ID) REFERENCES STREETS (STREET_ID),
                           CONSTRAINT ADDRESS_NUM_UN UNIQUE (HOUSE_NUMBER, STREET_ID),
                           CONSTRAINT CHK_HOUSE_NUMBER_FORMAT
                                CHECK (REGEXP_LIKE(HOUSE_NUMBER, '^[1-9][0-9]{0,3}(/[1-9][0-9]{0,3}[a-z]?)?$'))
);
/

-- =============================================
-- 2. USERS
-- =============================================

CREATE TABLE IF NOT EXISTS USERS (
                       USER_ID          INTEGER NOT NULL,
                       EMPLOYEE_ID      VARCHAR2(20 CHAR) NOT NULL UNIQUE, -- For login
                       PASSWORD_HASH    VARCHAR2(255 CHAR) NOT NULL,
                       FIRST_NAME       VARCHAR2(50 CHAR) NOT NULL,
                       LAST_NAME        VARCHAR2(50 CHAR) NOT NULL,
                       USER_TYPE        VARCHAR2(20 CHAR) NOT NULL,
                       PHONE            VARCHAR2(20 CHAR) NOT NULL,
                       EMAIL            VARCHAR2(254 CHAR) NOT NULL UNIQUE,
                       IS_ACTIVE        NUMBER(1) DEFAULT 1 NOT NULL,
                       VERSION          INTEGER DEFAULT 0 NOT NULL,
                       CONSTRAINT USER_PK PRIMARY KEY (USER_ID),
                       CONSTRAINT USER_ID_TYPE_UN UNIQUE (USER_ID, USER_TYPE),
                       CONSTRAINT CHK_USER_TYPE CHECK (USER_TYPE IN ('ADMIN', 'ADVISOR')),
                       CONSTRAINT CHK_USER_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);
/

CREATE TABLE IF NOT EXISTS ADVISORS (
                          USER_ID          INTEGER NOT NULL,
                          USER_TYPE        VARCHAR2(20 CHAR) DEFAULT 'ADVISOR' NOT NULL,
                          ICO              VARCHAR2(8 CHAR) NOT NULL UNIQUE,
                          MANAGER_ID       INTEGER,
                          CONSTRAINT ADVISOR_PK PRIMARY KEY (USER_ID),
                          CONSTRAINT ADVISOR_TYPE_CHK CHECK (USER_TYPE = 'ADVISOR'),
                          CONSTRAINT ADVISOR_ICO_CK CHECK (REGEXP_LIKE(ICO, '^[[:digit:]]{8}$')),
                          CONSTRAINT ADVISOR_MANAGER_FK FOREIGN KEY (MANAGER_ID) REFERENCES ADVISORS (USER_ID),
                          CONSTRAINT ADVISOR_USER_FK FOREIGN KEY (USER_ID, USER_TYPE)
                              REFERENCES USERS (USER_ID, USER_TYPE) ON DELETE CASCADE
);
/

