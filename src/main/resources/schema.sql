-- =============================================
-- 7. VIEWS
-- =============================================

CREATE OR REPLACE VIEW V_CLIENT_SEARCH_MINIMAL AS
SELECT
    C.CLIENT_ID,
    C.ADVISOR_ID,
    C.PERSONAL_ID,
    C.FIRST_NAME || ' ' || C.LAST_NAME AS FULL_NAME,
    CI_P.NAME AS PERM_CITY_NAME,
    CI_P.PSC AS PERM_PSC
FROM CLIENTS C
         JOIN ADDRESSES A_P ON C.PERMANENT_ADDR_ID = A_P.ADDRESS_ID
         JOIN STREETS ST_P ON A_P.STREET_ID = ST_P.STREET_ID
         JOIN CITIES CI_P ON ST_P.CITY_ID = CI_P.CITY_ID;
/

